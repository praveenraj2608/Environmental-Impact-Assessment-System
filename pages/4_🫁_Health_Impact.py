                    ann_config = ann_arts["config"]

                    feature_names = getattr(
                        ann_preprocessor, "feature_names",
                        ann_config.get("feature_names", list(inputs.keys()))
                    )
                    row = {f: inputs.get(f, 0.0) for f in feature_names}
                    X_df = pd.DataFrame([row])
                    X_scaled = ann_preprocessor.transform(X_df)

                    y_pred_arr, y_proba_arr = _pw_ann(ann_model, X_scaled)
                    pred_enc = int(y_pred_arr[0])
                    proba = y_proba_arr[0]

                    class_names = ann_config.get(
                        "class_names",
                        list(ann_preprocessor.label_encoder.classes_)
                    )
                    pred_class = ann_preprocessor.label_encoder.inverse_transform([pred_enc])[0]
