                    proba_raw = (
                        model.predict_proba(X_scaled)[0]
                        if hasattr(model, "predict_proba") else None
                    )
                    pred_class = encoder.inverse_transform([pred_enc])[0]
                    class_names = list(encoder.classes_)
                    proba = proba_raw if proba_raw is not None else np.array([])
                    confidence = float(np.max(proba)) * 100 if len(proba) else 0.0
                    used_model_name = type(model).__name__

